#include <iostream>
using namespace std;
int main()
{
    // Bitwise Operator
    // AND
    int ans = 2 & 3;
    cout << ans << endl;
    // OR
    int a = 2 | 3;
    cout << a << endl;
    // XOR
    int n = 2 ^ 3;
    cout << n << endl;
    // Left Shift
    int s = 2 << 3;
    cout << s << endl;
    // Right Shift
    int p = 18 >> 3;
    cout << p << endl;
    // compliment
    int b = ~5;
    cout << b << endl;
}